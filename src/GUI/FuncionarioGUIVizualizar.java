    public void ListarFuncionarios(){
        
        try {
            Funcionario funcionario = new Funcionario();
            ArrayList<Funcionario> resposta;
            DadosFuncionario dadosFuncionario = new DadosFuncionario();

            resposta = dadosFuncionario.listarFuncionarios(funcionario);

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Matricula", "Nome", "Cpf", "Rg" , "Sal√°rio"});

            for (Funcionario f : resposta) {
                modelo.addRow(new Object[]{f.getMatricula(),f.getNome(), f.getCpf(), f.getRg(), f.getSalario()});

            }

            jTableListarFuncionarios.setModel(modelo);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    
    }
