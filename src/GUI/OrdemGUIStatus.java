    private void jButtonAlterarStatusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarStatusActionPerformed
        // TODO add your handling code here:

        try {
            
            if (jComboBoxConsultarOS.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Sem Ordem de serviço para alterar");
                dispose();
                return;
            }
            
            if (jCheckBoxCancelar.isSelected() == true) {

                OrdemDeServico os = new OrdemDeServico();

                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                Date dataCancelamento = new java.sql.Date(formato.parse(jTextFieldData.getText()).getTime());

                os.setNumero(Integer.parseInt(jComboBoxConsultarOS.getSelectedItem().toString()));
                os.setDataCancelamento(dataCancelamento);
                os.setMotivoCancelamento(jTextFieldMotivoDescricao.getText());

                NegocioOrdemDeServico negocio = new NegocioOrdemDeServico();
                negocio.cancelarOrdemServico(os);

                JOptionPane.showMessageDialog(rootPane, "Ordem de serviço cancelada!");
                ListarNumeroOrdemDeServico();
                jTextFieldMotivoDescricao.setText("");

            }

            if (jCheckBoxSolucionar.isSelected() == true) {

                OrdemDeServico os = new OrdemDeServico();
                SimpleDateFormat formato = new SimpleDateFormat("dd/MM/yyyy");
                Date dataSolucao = new java.sql.Date(formato.parse(jTextFieldData.getText()).getTime());
                os.setDataSolucao(dataSolucao);
                os.setDescricaoServico(jTextFieldMotivoDescricao.getText());
                os.setNumero(Integer.parseInt(jComboBoxConsultarOS.getSelectedItem().toString()));

                NegocioOrdemDeServico negocio = new NegocioOrdemDeServico();
                negocio.solucionarOrdemServico(os);

                JOptionPane.showMessageDialog(rootPane, "Ordem de serviço solucionada!");
                ListarNumeroOrdemDeServico();
                jTextFieldMotivoDescricao.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }


    }//GEN-LAST:event_jButtonAlterarStatusActionPerformed

