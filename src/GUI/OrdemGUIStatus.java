    private void jButtonConsultarOsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarOsActionPerformed
        // TODO add your handling code here:

        try {

            if (jComboBoxConsultarOS.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Sem Ordem de serviço para consultar");
                dispose();
                return;
            }

            OrdemDeServico os = new OrdemDeServico();
            os.setNumero(Integer.parseInt(jComboBoxConsultarOS.getSelectedItem().toString()));

            DadosOrdemDeServico DadosOS = new DadosOrdemDeServico();

            this.ListarOs = DadosOS.listarOrdemServicos(os);

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Descrição Problema", "Status", "Numero", "Cliente_Cpf", "Matricula_funcionario", "Problema", "Data_Cadastro"});
            for (OrdemDeServico o : ListarOs) {

                if ("Aberta".equals(o.Status())) {
                    modelo.addRow(new Object[]{o.getDescricaoProblema(), o.getStatus(), o.getNumero(), o.getCliente().getCpf(), o.getFuncionario().getMatricula(), o.getClassificacaoProblema().getNome(), o.getDataCadastro()});

                }
            }

            jTableConsulta.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }

    }//GEN-LAST:event_jButtonConsultarOsActionPerformed

