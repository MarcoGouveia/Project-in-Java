    private void jButtonConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonConsultarActionPerformed

        try {

            if (jComboBoxConsultarOS.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Sem Ordem de Serviço para consulta");
                dispose();
                return;
            }

            OrdemDeServico os = new OrdemDeServico();
            os.setNumero(Integer.parseInt(jComboBoxConsultarOS.getSelectedItem().toString()));

            DadosOrdemDeServico DadosOS = new DadosOrdemDeServico();

            this.ListarOs = DadosOS.listarOrdemServicos(os);

            DefaultTableModel modelo = new DefaultTableModel();
            modelo.setColumnIdentifiers(new Object[]{"Numero", "Cliente_Cpf", "Matricula_funcionario", "Problema", "Data_Cadastro"});
            for (OrdemDeServico o : ListarOs) {

                if ("Aberta".equals(o.Status())) {
                    modelo.addRow(new Object[]{o.getNumero(), o.getCliente().getCpf(), o.getFuncionario().getMatricula(), o.getClassificacaoProblema().getNome(), o.getDataCadastro()});

                }
            }

            jTableOrdemDeServico.setModel(modelo);

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButtonConsultarActionPerformed

    private void jButtonRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemoverActionPerformed
        try {

            if (jComboBoxConsultarOS.getSelectedIndex() == -1) {
                JOptionPane.showMessageDialog(rootPane, "Sem Ordem de serviço para remover");
                dispose();
                return;
            }
            OrdemDeServico OS = new OrdemDeServico();
            OS.setNumero(Integer.parseInt(jComboBoxConsultarOS.getSelectedItem().toString()));

            NegocioOrdemDeServico Negocio = new NegocioOrdemDeServico();
            Negocio.removerOrdemServico(OS);

            int resposta = JOptionPane.showConfirmDialog(rootPane, "Ordem de serviço removida! \nDeseja Remover outra ordem de serviço ? ", "Sucesso", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);

            if (resposta == JOptionPane.YES_OPTION) {
                ResetarTabela();
                ListarNumeroOrdemDeServico();
            } else {
                dispose();
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }//GEN-LAST:event_jButtonRemoverActionPerformed

