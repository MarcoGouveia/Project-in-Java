    public void ListarNumeroOrdemDeServico() {
        try {
            OrdemDeServico os = new OrdemDeServico();
            DadosOrdemDeServico dados = new DadosOrdemDeServico();
            this.ListarOs = dados.listarOrdemServicos(os);
            DefaultComboBoxModel modelo = new DefaultComboBoxModel();

            for (OrdemDeServico o : this.ListarOs) {

                if ("Aberta".equals(o.Status())) {
                    modelo.addElement(o.getNumero());
                }
            }

            jComboBoxConsultarOS.setModel(modelo);

        } catch (Exception e) {

            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
    }

